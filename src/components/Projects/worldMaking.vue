            <br>
            <i>Ori and the Will of the Wisps</i> also added diverse combat mechanisms and platform skills to enable a fluid, fast-paced and progressive game flow. Due to the high latency of Ori’s jump action, players are forced to accumulate more skills to pass different barriers on the map. In-air actions and skills like sliding, dashing, and bashing maximizes the possibilities of Ori’s movement and encourages its interactions with the physical elements in the environment. Noticeably, skills like bashing that have both effects in motion and attacking further make Ori’s combat experience flexible and allow players’ unique tactics for different enemies.
            <br>
            <br>
            One thing I am personally sad to see is that <i>Ori and the Will of the Wisps</i> cancels the previous checkpoint mechanism. In Ori and the Blind Forest, players can only save their progress with energy cells or enter certain stages of the game. The balance between ability usage and game progress needs to be taken into extra consideration. Though it could be annoying if a player travels all the way to the other end of the map and forget to create a checkpoint by casting a skill, I still consider it a brilliant and novel design. However, <i>Ori and the Will of the Wisps</i> changes back to a traditional checkpoint solution, which is automatically saving progress when a player reaches a stable platform. I conjecture this adjustment is a compromise to the game’s difficulty.
            <br>
            <br>
            Overall, <i>Ori and the Will of the Wisps</i> is another splendid piece by Moon Studios. By mixing 2D and 3D perspectives, adding more abilities and skills, and adjusting the game-saving mechanism, it reaches another level of captivation and versatility for 2D platform-adventure games.

