            <i>Ori and the Will of the Wisps</i> is a sequel to the phenomenal indie Metroidvania <i>Ori and the Blind Forest</i>. Compare to its previous version, its further improvement in immersion and flexibility made it the next milestone of its kind.
            <br>
            <br>
            <img src="https://assets.nintendo.com/image/upload/ar_16:9,c_limit,w_801/b_white/f_auto/q_auto/ncom/en_US/games/switch/o/ori-and-the-will-of-the-wisps-switch/hero">
