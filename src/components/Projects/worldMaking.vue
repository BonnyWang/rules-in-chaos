            Unlike other common 2D platformer games, <i>Ori and the Will of the Wisps</i> mixed 2D and 3D perspectives to create a more immersive scene for players. Although a player can only move in the XY plane, the background extends to the Z axis and is rendered in a 3D perspective. There are also shadows of plants and other creatures at the layer in front of the main ground, generating an illusion of peeking at the scene from another side of the forest rather than viewing an orographic close image through your monitor. 
            <br>
            <br>
            Besides creating this special angle of view for the game experience, <i>Ori and the Will of the Wisps</i> also utilize this feature to build more engaging Boss combats. All bosses are rendered in the 3D scene and gigantic in scale compared to Ori, creating a sense of oppression. When fighting Shriek, the player will see it initially as a small dot at the center of the screen since it is still far away, then rapidly approaching closer, taking up the most space of the screen and even shaking the entire scene. However, at the same time, this mix of perspectives could create some confusion for players. Since the collisions are still determined by overlaps in 2D, the motion curves of the skills and objects could be counter-intuitive. Personally, I only took a little time adapting it but it is something that could be improved by a better ordering of the object layers.
            <br>
            <br>
            <img src="https://i.ytimg.com/vi/vDdQg9WOZyk/maxresdefault.jpg">
